function BubbleToGS(answers){

    let keys = Object.keys(answers[0])





    var allQuestions = []
    const exclude = ['Created By', 'Modified Date', , '_type']
    
    answers.forEach(item => {
        Object.keys(item).forEach(question => {
    
            if (!allQuestions.includes(question)) {
                allQuestions.push(question)
    
            } else {
                // allQuestions.push(question)
            }
    
        })
    })
    allQuestions.sort()
    
    
    var result = {}
    //result[0]=allQuestions
    allQuestions.forEach(key => {
        result[key] = []
    })
    
    answers.forEach(item => {
    
        Object.keys(item).forEach(key => {
            //console.log(Array.isArray(item[key]))
            if (result[key] !== undefined) {
                if (!Array.isArray(item[key])) {
                    result[key].push(item[key])
                }else{
                    result[key].push(item[key].join(', '))
                }
            }
        })
    
    })
    
    let gsAnswers = result
    exclude.forEach(item=>{
        delete gsAnswers[item]
    })
    
    
    Object.defineProperty(gsAnswers, '_created_at',
        Object.getOwnPropertyDescriptor(gsAnswers, 'Created Date'));
    delete gsAnswers["Created At"];
    
    var gsResult = []
    gsResult[0]=Object.keys(gsAnswers)
    
    
    let gsAnswers2 =Object.keys(gsAnswers).map(item=>{
       
        return [item].concat(gsAnswers[item])
    
    
    })
    
return gsAnswers2
}


